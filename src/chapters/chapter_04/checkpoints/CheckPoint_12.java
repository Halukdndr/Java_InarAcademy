package chapters.chapter_04.checkpoints;

public class CheckPoint_12 {

	public static void main(String[] args) {
		char c = 'A';
		int i = (int)c;
		System.out.println(i);//65
		
		float f = 1000.34f;
		int t = (int)f;
		System.out.println(t);//1000
		
		double d = 1000.34;
		int z = (int)d;
		System.out.println(z);//1000
		
		int k = 97;
		char l = (char)k;
		System.out.println(l);//a
		
	}

}
