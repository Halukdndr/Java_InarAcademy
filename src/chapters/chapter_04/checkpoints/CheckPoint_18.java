
package chapters.chapter_04.checkpoints;

public class CheckPoint_18 {

	public static void main(String[] args) {
		String str = "1";
		char ch = '1';
		System.out.println(str + 1);//11
		System.out.println(ch + 1);//50
		System.out.println("1" + 1 + 1);//111
		System.out.println(str + 1 + (1 + 1));//112
		System.out.println('1' + 1 + 1);//51
		

	}

}
